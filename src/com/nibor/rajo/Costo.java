package com.nibor.rajo;

public class Costo {

	private Double mina;
	private Double remocion;
	private Double planta;
	private Double fundicionRefineria;
	private Double comercializacion;
	private Double generalesyAdministracion;
	
	public Double getMina() {
		return mina;
	}
	public void setMina(Double mina) {
		this.mina = mina;
	}
	public Double getRemocion() {
		return remocion;
	}
	public void setRemocion(Double remocion) {
		this.remocion = remocion;
	}
	public Double getPlanta() {
		return planta;
	}
	public void setPlanta(Double planta) {
		this.planta = planta;
	}
	public Double getFundicionRefineria() {
		return fundicionRefineria;
	}
	public void setFundicionRefineria(Double fundicionRefineria) {
		this.fundicionRefineria = fundicionRefineria;
	}
	public Double getComercializacion() {
		return comercializacion;
	}
	public void setComercializacion(Double comercializacion) {	
		this.comercializacion = comercializacion;
	}
	public Double getGeneralesyAdministracion() {
		return generalesyAdministracion;
	}
	public void setGeneralesyAdministracion(Double generalesyAdministracion) {
		this.generalesyAdministracion = generalesyAdministracion;
	}
}